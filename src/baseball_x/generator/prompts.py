def team_hashtag(team_short: object) -> str | None:
    """team_short からチームの公式ハッシュタグを返す。未知のチームは None。"""
    return TEAM_HASHTAGS.get(str(team_short).strip())


def append_team_hashtag(body: str, team_short: object) -> str:
    """本文に該当チームのハッシュタグを1つ追記する（既に含む場合は何もしない）。"""
    tag = team_hashtag(team_short)
    if not tag or tag in body:
        return body
    return f"{body} {tag}"


